// რა არის HTTP:

// HTTP (HyperText Transfer Protocol) არის პროტოკოლი, რომელიც გამოიყენება ვებ-ბრაუზერსა და ვებ-სერვერს შორის ინფორმაციის გადასაცემად. ეს არის კლიენტ-სერვერის მოდელზე დაფუძნებული პროტოკოლი, რომელიც განსაზღვრავს წესებს, თუ როგორ უნდა მოხდეს მონაცემების გაგზავნა და მიღება ინტერნეტში. როდესაც ბრაუზერი აგზავნის მოთხოვნას ვებსაიტზე, სწორედ HTTP უზრუნველყოფს ამ კომუნიკაციის განხორციელებას.

// რა არის HTTP Request და Request Methods:

// HTTP Request არის კლიენტის (მაგალითად, ბრაუზერის) მიერ გაგზავნილი მოთხოვნა სერვერისკენ, რომელშიც მოთხრობილია, რას ითხოვს კლიენტი სერვერისგან (მაგ. ვებგვერდი, ფაილი, მონაცემები და ა.შ).

// Request Methods (მოთხოვნის მეთოდები) განსაზღვრავს, რა ტიპის მოქმედებას ითხოვს კლიენტი. ძირითადი ნასწავლი მეთოდებია:

// - GET: ითხოვს მონაცემებს სერვერისგან. (მაგ: ვებგვერდის ჩატვირთვა).
// - POST: აგზავნის მონაცემებს სერვერზე (მაგ: ფორმის შევსება).
// - PUT: ანახლებს არსებულ მონაცემებს სერვერზე.
// - DELETE: შლის მონაცემებს სერვერიდან.

// რა არის HTTP Response და Response Codes:

// HTTP Response არის სერვერის პასუხი კლიენტის მოთხოვნაზე. პასუხი შეიცავს სტატუსის კოდს, სათაურს (headers) და საჭიროების შემთხვევაში — შიგთავსს (content).

// Response Codes (სტატუსის კოდები) არის სამნიშნა რიცხვები, რომლებიც მიუთითებს პასუხის ტიპზე:

// - 200 OK: მოთხოვნა წარმატებით შესრულდა.
// - 201 Created: მონაცემები წარმატებით შეიქმნა (POST-ის დროს).
// - 400 Bad Request: მოთხოვნა არასწორად იყო ფორმირებული.
// - 401 Unauthorized: ავტორიზაცია საჭიროებს.
// - 403 Forbidden: აკრძალულია მოთხოვნილი რესურსი.
// - 404 Not Found: რესურსი ვერ მოიძებნა.
// - 500 Internal Server Error: სერვერზე მოხდა გაუთვალისწინებელი შეცდომა.
