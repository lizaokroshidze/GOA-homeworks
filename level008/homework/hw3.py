# რა 3 ნაწილისგან შედგება კოდის წერის პროცესი(პასუხი დაასაბუთეთ.)

# 1) დაგეგმვა და ანალიზი - თავდაპირველად ჩვენ უნდა ვიცოდეთ თუ რის შექმნას ვაპირებთ,ამის შემდეგ კი მივყვებით გეგმას.

# 2) პროგრამირება - ეს ეტაპი მოიცავს კოდის წერას.პროგრამისტი იყენებს კოდირების ენის სინტაქსს და ლოგიკურ სტრუქტურას,რათა შექმნას ფუნქციონალური კოდი.

# 3) ტესტირება და გაუმჯობესება - კოდის ტესტირება აუცილიებელია,რადგან აქ შეიზლება გამოვლინდეს შეცდომები და მათ გამოსასწორებლად.