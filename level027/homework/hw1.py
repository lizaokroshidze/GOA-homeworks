#1) for loop ციკლის საშუალებით შასრულეთ ყველა მათემათიკური ოპერაცია 0 - დან 100 მდე.


for i in range (101):
    addition = i + 2        
    subtraction = i - 2    
    multiplication = i * 2
    division = i / 2 

    print(i,addition)
    print(i,subtraction)
    print(i,multiplication)
    print(i,division)

